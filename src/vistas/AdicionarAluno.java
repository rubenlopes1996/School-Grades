    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if(txtNome.getText().isEmpty() || txtData.getText().isEmpty() || txtNumero.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos");
        }else{
            Data data = Data.parse(txtData.getText());
            Aluno aluno = new Aluno(txtNome.getText(), Integer.parseInt(txtNumero.getText()), data, txtCurso.getText());
            DadosAplicacao.getINSTANCE().adicionarAluno(aluno);
            
            setModal(false);
            this.dispose(); 
            
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

