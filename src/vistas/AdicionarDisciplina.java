    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if(txtAno.getText().isEmpty() || txtECTS.getText().isEmpty() || txtNome.getText().isEmpty() || txtNota.getText().isEmpty() || txtSemestre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos");
        }
        
        else{
        
        try {
            if(Integer.parseInt(txtAno.getText().toString()) != 1 && Integer.parseInt(txtAno.getText().toString()) != 2 && Integer.parseInt(txtAno.getText().toString()) != 3 ){
               JOptionPane.showMessageDialog(this, "O campo Ano tem de ser um n√∫mero entre 1 e 3");
            }
            
            if(Integer.parseInt(txtECTS.getText().toString()) <= 0){
               JOptionPane.showMessageDialog(this, "O campo ECTS tem de ser positivo");
            }
            
            if(Integer.parseInt(txtNota.getText().toString()) < 0 || Integer.parseInt(txtNota.getText().toString()) > 20){
               JOptionPane.showMessageDialog(this, "O campo Nota tem de ser um valor entre 0 e 20");
            }  
            
             if(Integer.parseInt(txtSemestre.getText().toString()) != 1 && Integer.parseInt(txtSemestre.getText().toString()) != 2 ){
               JOptionPane.showMessageDialog(this, "O campo Semestre tem de ser 1 ou 2");
            }
        }
        catch (NumberFormatException e) {
           JOptionPane.showMessageDialog(this, "O campo Ano, ECTS, Nota e Semestre t√™m de ser um n√∫mero");
        }
        
        Disciplina disciplina = new Disciplina(txtNome.getText(), Integer.parseInt(txtAno.getText()), Integer.parseInt(txtSemestre.getText()), Integer.parseInt(txtECTS.getText()), Integer.parseInt(txtNota.getText()));
        DadosAplicacao.getINSTANCE().adicionarDisciplina(disciplina);
        setModal(false);
        this.dispose(); 
        }
       
    }//GEN-LAST:event_btnAdicionarActionPerformed

